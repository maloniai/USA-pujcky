// Helper functions
export function getPolandCityBySlug(slug: string): PolandCityInfo | undefined {
  return polandCities.find((city) => city.slug === slug)
}

export function getPolandCitiesByRegion(regionSlug: string): PolandCityInfo[] {
  return polandCities.filter((city) => city.regionSlug === regionSlug)
}

export function getAllPolandCitySlugs(): string[] {
  return polandCities.map((city) => city.slug)
}

export function getPolandCityNames(): string[] {
  return polandCities.map((city) => city.namePl)
}
