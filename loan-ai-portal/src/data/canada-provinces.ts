export function getProvinceBySlug(slug: string): CanadianProvinceInfo | undefined {
  return canadianProvinces.find(p => p.slug === slug)
}

export function getProvincesByRegion(region: keyof typeof provinceRegions): CanadianProvinceInfo[] {
  const provinceNames = provinceRegions[region]
  return canadianProvinces.filter(p => provinceNames.includes(p.name))
}
